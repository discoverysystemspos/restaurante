/** =====================================================================
 *  BANCO ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER
const { getBancos, getBancoId, createBanco, updateBanco, deleteBanco } = require('../controllers/bancos.controller');


const router = Router();

/** =====================================================================
 *  GET BANCOS
=========================================================================*/
router.get('/', validarJWT, getBancos);
/** =====================================================================
 *  GET BANCOS
=========================================================================*/

/** =====================================================================
 *  GET BANCO ID
=========================================================================*/
router.get('/:id', validarJWT, getBancoId);
/** =====================================================================
 *  GET BANCO ID
=========================================================================*/

/** =====================================================================
 *  CREATE BANCO
=========================================================================*/
router.post('/', [
        validarJWT,
        check('name', 'El nombre es obligatorio').not().isEmpty(),
        validarCampos
    ],
    createBanco
);
/** =====================================================================
 *  CREATE BANCO
=========================================================================*/

/** =====================================================================
 *  UPDATE BANCO
=========================================================================*/
router.put('/:id', [
        validarJWT,
        validarCampos
    ],
    updateBanco
);
/** =====================================================================
 *  UPDATE BANCO
=========================================================================*/

/** =====================================================================
 *  DELETE BANCO
=========================================================================*/
router.delete('/:id', validarJWT, deleteBanco);
/** =====================================================================
 *  DELETE BANCO
=========================================================================*/

// EXPORTS
module.exports = router;