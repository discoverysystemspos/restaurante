/** =====================================================================
 *  MESAS ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER
const { getMesas, createMesas, getMesaId, updateMesa, getMesasComanda, updateNota, deleteIngrediente, updateMenu } = require('../controllers/mesas.controller');

const router = Router();

/** =====================================================================
 *  GET MESAS
=========================================================================*/
router.get('/', getMesas);
/** =====================================================================
 *  GET MESAS 
=========================================================================*/

/** =====================================================================
 *  GET MESA COMANDA
=========================================================================*/
router.get('/comandas/', validarJWT, getMesasComanda);
/** =====================================================================
 *  GET MESA COMANDA
=========================================================================*/

/** =====================================================================
 *  GET MESA ID
=========================================================================*/
router.get('/:id', getMesaId);
/** =====================================================================
 *  GET MESA ID
=========================================================================*/


/** =====================================================================
 *  CREATE MESAS
=========================================================================*/
router.post('/', [
        validarJWT,
        check('name', 'El nombre es obligatorio').not().isEmpty(),
        validarCampos
    ],
    createMesas
);
/** =====================================================================
 *  CREATE MESAS
=========================================================================*/

/** =====================================================================
 *  UPDATE MESAS
=========================================================================*/
router.put('/:id', updateMesa);
/** =====================================================================
 *  UPDATE MESAS
=========================================================================*/

/** =====================================================================
 *  UPDATE NOTAS MESAS
=========================================================================*/
router.put('/notas/:id', validarJWT, updateNota);
/** =====================================================================
 *  UPDATE NOTAS MESAS
=========================================================================*/

/** =====================================================================
 *  UPDATE NOTAS MESAS
=========================================================================*/
router.put('/mesa/:id/menu/:menu', updateMenu);
/** =====================================================================
 *  UPDATE NOTAS MESAS
=========================================================================*/

/** =====================================================================
 *  DELETE INGREDIENTE
=========================================================================*/
router.delete('/:id/comanda/:comanda/ingrediente/:ingrediente', deleteIngrediente);
/** =====================================================================
 *  DELETE INGREDIENTE
=========================================================================*/

// EXPORTS
module.exports = router;