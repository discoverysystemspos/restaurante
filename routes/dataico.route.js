/** =====================================================================
 *  BANCO ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER

const { getDataico, createDataico, updateDataico } = require('../controllers/dataico.controller');


const router = Router();

/** =====================================================================
 *  GET BANCOS
=========================================================================*/
router.get('/', validarJWT, getDataico);
/** =====================================================================
 *  GET BANCOS
=========================================================================*/

/** =====================================================================
 *  CREATE BANCO
=========================================================================*/
router.post('/', validarJWT, createDataico);
/** =====================================================================
 *  CREATE BANCO
=========================================================================*/

/** =====================================================================
 *  UPDATE BANCO
=========================================================================*/
router.put('/:id', validarJWT, updateDataico);
/** =====================================================================
 *  UPDATE BANCO
=========================================================================*/

// EXPORTS
module.exports = router;