/** =====================================================================
 *  INVOICE ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER
const {
    getInvoices,
    createInvoice,
    getInvoiceId,
    getInvoicesDate,
    returnInvoice,
    updateInvoice,
    deleteProductInvoice,
    getInvoicesAll,
    updateProductQty,
    getInvoicesTurn,
    getInvoiceCreditClient,
    getInvoiceVenida,
    getInvoicesCredito,
    getInvoiceCreditCajeroMesa,
    postQueryInvoice,
    getCountInvoicesElectronic,
    invoicePDF
} = require('../controllers/invoices.controller');

const router = Router();

/** =====================================================================
 *  GET INVOICES
=========================================================================*/
router.get('/', validarJWT, getInvoices);
/** =====================================================================
 *  GET INVOICES
=========================================================================*/

/** =====================================================================
 *  GET INVOICES
=========================================================================*/
router.get('/pdf/:id', invoicePDF);
/** =====================================================================
 *  GET INVOICES
=========================================================================*/

/** =====================================================================
 *  GET INVOICES TURNS
=========================================================================*/
router.get('/credito', validarJWT, getInvoicesCredito);
/** =====================================================================
 *  GET INVOICES TURNS
=========================================================================*/

/** =====================================================================
 *  GET INVOICES TURNS
=========================================================================*/
router.get('/cierre', validarJWT, getInvoicesTurn);
/** =====================================================================
 *  GET INVOICES TURNS
=========================================================================*/

/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/
router.get('/fecha/', validarJWT, getInvoicesAll);
/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/

/** =====================================================================
 *  GET INVOICES VENCIDAS
=========================================================================*/
router.get('/vencidas/:fecha', validarJWT, getInvoiceVenida);
/** =====================================================================
 *  GET INVOICES VENCIDAS
=========================================================================*/

/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/
router.get('/date/:fecha', validarJWT, getInvoicesDate);
/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/

/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/
router.get('/count/electronica', getCountInvoicesElectronic);
/** =====================================================================
 *  GET INVOICES DATE
=========================================================================*/

/** =====================================================================
 *  GET INVOICES ID
=========================================================================*/
router.get('/:id', validarJWT, getInvoiceId);
/** =====================================================================
 *  GET INVOICES ID
=========================================================================*/

/** =====================================================================
 *  GET INVOICES CREDI FOR CLIENT 
=========================================================================*/
router.get('/:client/:credito', validarJWT, getInvoiceCreditClient);
/** =====================================================================
 *  GET INVOICES CREDI FOR CLIENT
=========================================================================*/

/** =====================================================================
 *  GET INVOICES CREDI FOR CAJERO MESA 
=========================================================================*/
router.get('/vendedor/mesa/:mesa', validarJWT, getInvoiceCreditCajeroMesa);
/** =====================================================================
 *  GET INVOICES CREDI FOR CAJERO MESA
=========================================================================*/

/** =====================================================================
 *  CREATE INVOICE
=========================================================================*/
router.post('/:turno', [
        validarJWT,
        check('type', 'Tienes que especificar que metodo de pago uso').not().isEmpty(),
        check('amount', 'El monto es obligatorio').not().isEmpty(),
        check('products', 'No ha seleccionado ningun producto').not().isEmpty(),
        validarCampos
    ],
    createInvoice);
/** =====================================================================
*  CREATE INVOICE
=========================================================================*/

/** =====================================================================
 *  POST QUERY INVOICE
=========================================================================*/
router.post('/search/query', validarJWT, postQueryInvoice);
/** =====================================================================
*  POST QUERY INVOICE
=========================================================================*/

/** =====================================================================
*  PUT INVOICE
=========================================================================*/
router.put('/pago/:id', validarJWT, updateInvoice);
/** =====================================================================
*  PUT INVOICE
=========================================================================*/

/** =====================================================================
 *  RETURN INVOICE
 =========================================================================*/
router.delete('/:id', validarJWT, returnInvoice);
/** =====================================================================
  *  RETURN INVOICE
=========================================================================*/

/** =====================================================================
 *  DELETE PRODUCT OF INVOICE
 =========================================================================*/
router.delete('/:factura/product/:id', validarJWT, deleteProductInvoice);
/** =====================================================================
 *  DELETE PRODUCT OF INVOICE
 =========================================================================*/

/** =====================================================================
*  PUT INVOICE
=========================================================================*/
router.put('/:factura/product/:id/qty/:qty', validarJWT, updateProductQty);
/** =====================================================================
*  PUT INVOICE
=========================================================================*/



// EXPORT
module.exports = router;