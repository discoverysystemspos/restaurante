/** =====================================================================
 *  PRESTAMO ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLER
const { getPrestamos, getPrestamoId, createPrestamo, updatePrestamo, deletePrestamo, getPrestamosDates, getPrestamoClient } = require('../controllers/prestamos.controller');

const router = Router();

/** =====================================================================
 *  GET PRESTAMOS
=========================================================================*/
router.get('/', validarJWT, getPrestamos);
/** =====================================================================
 *  GET PRESTAMOS
=========================================================================*/

/** =====================================================================
 *  GET PRESTAMO ID
=========================================================================*/
router.get('/:id', validarJWT, getPrestamoId);
/** =====================================================================
 *  GET PRESTAMO ID
=========================================================================*/

/** =====================================================================
 *  GET PRESTAMOS CLIENTS
=========================================================================*/
router.get('/client/:client', validarJWT, getPrestamoClient);
/** =====================================================================
 *  GET PRESTAMOS CLIENTS
=========================================================================*/

/** =====================================================================
 *  GET PRESTAMOS DATES
=========================================================================*/
router.get('/date/:fecha', validarJWT, getPrestamosDates);
/** =====================================================================
 *  GET PRESTAMOS DATES
=========================================================================*/

/** =====================================================================
 *  CREATE PRESTAMO
=========================================================================*/
router.post('/', [
        validarJWT,
        check('client', 'El cliente es obligatorio').isMongoId(),
        check('monto', 'El monto es obligatorio').not().isEmpty(),
        check('porcentaje', 'El monto es obligatorio').not().isEmpty(),
        validarCampos
    ],
    createPrestamo
);
/** =====================================================================
 *  CREATE PRESTAMO
=========================================================================*/

/** =====================================================================
 *  UPDATE PRESTAMO
=========================================================================*/
router.put('/:id', validarJWT,
    updatePrestamo
);
/** =====================================================================
 *  UPDATE PRESTAMO
=========================================================================*/

/** =====================================================================
 *  DELETE PRESTAMO
=========================================================================*/
router.delete('/:id', validarJWT, deletePrestamo);
/** =====================================================================
 *  DELETE PRESTAMO
=========================================================================*/

// EXPORTS
module.exports = router;